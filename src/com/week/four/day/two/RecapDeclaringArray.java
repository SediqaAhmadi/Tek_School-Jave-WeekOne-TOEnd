package com.week.four.day.two;

public class RecapDeclaringArray {

		public static void main(String[] args) {
			
			char[][] characters = {
					
					{'A' , 'B' , 'C'} , 
					
					{'D' , 'E' , 'F'} , 
					
					{'G' , 'H' , 'I'}
			};
			
			System.out.println(characters[2][2]);
			
			System.out.println(characters[0][2]);
			
		}

	}

package com.week.two.day.two;

public class StringCharAtIndex {

public static void main(String[] args) {
		
		//Find Character for Given Index 
		
		String greeting = "Good Evening everyone !!";
		
		//find the character on index 8; 
		
		char charInIndex8 = greeting.charAt(8);
		
		System.out.println(charInIndex8);
		
		char charInIndex10 = greeting.charAt(11);
		System.out.println(charInIndex10);
		
		char charInIndex40 = greeting.charAt(40);
		System.out.println(charInIndex40);
		
	}

}

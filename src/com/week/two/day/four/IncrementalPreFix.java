package com.week.two.day.four;

public class IncrementalPreFix {

public static void main(String[] args) {
		
		int number = 0; 
		
		System.out.println(++ number);
		
		number ++;
		System.out.println(number);
		number ++; 
		System.out.println(number);
		number ++; 
		System.out.println(number);
		
	}

}

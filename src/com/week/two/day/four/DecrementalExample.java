package com.week.two.day.four;

public class DecrementalExample {

	public static void main(String[] args) {
		// Prefix means before. 
		//Postfix mean after.
		int number = 10; 
		
		//prefix
		System.out.println(--number);
		
		System.out.println(number --);
		
		System.out.println(number );
		
	}

}

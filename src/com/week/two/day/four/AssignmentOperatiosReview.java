package com.week.two.day.four;

public class AssignmentOperatiosReview {

public static void main(String[] args) {
		
		// = sign is to assign value. 
		
		int numberA = 10;
		
		int numberB = 20; 
		
		numberA = numberB; 
		
		//ShortCut Assignment. 
		
		int result = 0 ; 
		
		result += 10; // result = result + 10;  0 + 10
		
		//Subtraction assignment -= 
		
		int numberX = 100; 
		
		numberX -= 20; 
		System.out.println(numberX);
		// multiplication *=
		// division /=
		
	}

}

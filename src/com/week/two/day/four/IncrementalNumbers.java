package com.week.two.day.four;

public class IncrementalNumbers {

public static void main(String[] args) {
		
		int number = 10; 
		System.out.println(number);
		//Add 1 to number; 
		//Option 1
		number = number + 1 ; 
		System.out.println(number);
		// Option 2
		number += 1; 
		System.out.println(number);
		//Option 3 
		//Increment by one only 
		number ++;
		System.out.println(number);
		
	}

}

package com.week.two.day.three;

public class AssignmentOperator {

	
		
		public static void main(String[] args) {
			
			// += sign what that mean? 
			
			
			int a = 10 ; 
			
			int result = 40; 
			
			
			//result = result + a;
			result += a;  // 40 + 10 = 50;
			
			result *= a;  // 50 * 10;
			
			System.out.println(result);
			
		}

}
package com.week.two.day.one;

public class RecapForArithmetics {

public static void main(String[] args) {
		
		// + used to addition . 
		
		
		int numberOne = 400; 
		int numberTwo = 800; 
		
		int result = numberOne + numberTwo; 
		
		System.out.println(result);
		
		
		// - used to subtraction.
		
		int numberThree = 1200; 
		
		int numberFour = 600; 
		
		System.out.println(numberThree - numberFour);
		
		int resultOfSubtraction = numberThree - numberFour;
		
		System.out.println(resultOfSubtraction);
	}

}
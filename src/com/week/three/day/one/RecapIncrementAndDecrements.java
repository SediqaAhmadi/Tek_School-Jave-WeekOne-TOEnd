package com.week.three.day.one;

public class RecapIncrementAndDecrements {

	public static void main(String[] args) {

		int number = 0;

		number++; // Incremental ++; number = number + 1, number += ;
		number++;
		System.out.println(number);

		number--;

		System.out.println(number);

		// prefix and postfix

		System.out.println(--number);

		System.out.println(number++);

	}

}
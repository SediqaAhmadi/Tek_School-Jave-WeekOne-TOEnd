package com.week.three.day.one;

public class LessThanAndGreaterThan {

public static void main(String[] args) {
		
		//Relational Operator; 
		//Less than <
		//greater than > 
		
		byte age = 30; 
		
		boolean result = age < 50; 
		
		System.out.println(result);
		
		//Greater Than example: 
		double applePrice = 10.22; 
		
		double orangePrice = 10.21; 
		boolean isGreaterThan = applePrice > orangePrice; 
		
		System.out.println(isGreaterThan);
	}

}
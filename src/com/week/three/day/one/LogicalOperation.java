package com.week.three.day.one;

public class LogicalOperation {

public static void main(String[] args) {
		
		// Logical Operators are  
		//  && (AND) => return true only if both relational Operation is true
		//  || (OR) => return true only if one of the relational operations true. 
		// ! (NOT) => return true if relational operation false.  vice versa
		
		// Exampe for AND && 
		boolean firstBoolean = false;
		
		boolean secondBoolean = false;
		
		
		boolean checkAndCondition = firstBoolean && secondBoolean;
		
		System.out.println(checkAndCondition);
		
	}

}

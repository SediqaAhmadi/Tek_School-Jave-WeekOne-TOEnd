package com.week.one.day.three;

public class ArethmiticExamples {

	public static void main(String[] args) {

		// + use for adding
		// - use for subtract
		// * use for multipication
		// / use for division
		// % use for reminders.

		int firstNumber = 12345678;

		int secondNumber = 987654321;

		int result = firstNumber + secondNumber;

		System.out.println(result);

		long firstAccountBalance = 15000L;
		long secondAccountBalance = 50000L;

		long totalAccountBalance = firstAccountBalance + secondAccountBalance;

		System.out.println(totalAccountBalance);
	}

}
